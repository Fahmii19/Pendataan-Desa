    <h4>Ketentuan TPZ Zona Intensitas Sangat Tinggi (m.1)</h4>

    <p>Ketentuan TPZ m1 di dalam Zona Bonus (terdapat Kode b)</p>

    <ol>

        <li>Jika Intensitas Pemanfaatan Ruang berdasarkan ketentuan peraturan perundang-undangan sebelum Peraturan Gubernur ini ditetapkan kurang dari batasan Intensitas Bonus maka masih dapat menaikkan Intensitas Pemanfaatan Ruang sampai batasan Intensitas Bonus dengan kontribusi</li>

        <li>Jika Intensitas Pemanfaatan Ruang berdasarkan ketentuan peraturan perundang-undangan sebelum Peraturan Gubernur ini ditetapkan lebih dari batasan Intensitas Bonus maka tidak diperbolehkan meningkatkan Intensitas Pemanfaatan Ruang.</li>

        <li>Dalam hal terdapat peningkatan Intensitas Pemanfaatan Ruang pada Lahan Perencanaan seperti pada poin diatas maka akan dikenakan Disinsentif.</li>

        <li>Disinsentif dikonversi dalam satuan rupiah dan digunakan untuk penyediaan Prasarana Sarana Umum dan/ atau membantu program Pemerintah Provinsi DKI Jakarta untuk menyelesaikan masalah perkotaan.</li>

        <li>Penentuan penyediaan Prasarana Sarana Umum dilaksanalcan oleh Kepala Perangkat Daerah yang menyelenggarakan urusan pemerintahan di bidang Penataan Ruang dan Bangunan Gedung dan memperoleh Persetujuan Gubernur.</li>

        <li>Pengenaan Disinsentif tercantum dalam KKKPR dapat dilaksanakan pada saat proses Perizinan Berusaha atau setelah Perizinan Berusaha diterbitkan.

        </li>

    </ol>

    <p style=" font-style: italic;">
