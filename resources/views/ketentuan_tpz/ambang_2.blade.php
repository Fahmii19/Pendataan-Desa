    <h4>Pengajuan Dokumen Pengembangan Kawasan Untuk Kawasan Zona Ambang</h4>
    <p>Pengajuan dokumen rancang bangun kawasan zona ambang dilakukan dengan:</p>

    <ol>

        <li>Dokumen Pengembangan Kawasan merupakan proposal yang diajukan oleh calon Pengelola Kawasan kepada Kepala Perangkat Daerah yang menyelenggarakan urusan pemerintahan di bidang Penataan Ruang dan Bangunan Gedung.</li>

        <li>Proposal selanjutnya disampaikan kepada FPRD untuk mendapatkan pertimbangan.</li>

        <li>Hasil pertimbangan FPRD disampaikan kepada Gubernur untuk mendapatkan Persetujuan.</li>

        <li>Dalam hal Gubernur menyetujui hasil pertimbangan FPRD penetapan lokasi dan Pengelola Kawasan Zona Ambang ditetapkan dengan Keputusan Gubernur.</li>

        <li>Keputusan Gubernur tentang penetapan lokasi dan Pengelola Zona Ambang menjadi bahan evaluasi dalam peninjauan kembali RDTR.</li>


    </ol>

    <p>
        <strong>Sumber</strong> Pasal 196, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun
        2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta
    </p>

