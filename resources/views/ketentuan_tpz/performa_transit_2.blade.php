    <h4>Dokumen Rancang Bangun Pada Kawasan Berorientasi Transit</h4>

    <p>Dokumen rancang bangun terdiri atas:</p>

    <ol style="list-style-type: lower-alpha;">
        <li>Gambaran umum pengembangan Kawasan merupakan kondisi eksisting dalam Lahan Perencanaan</li>

        <li>Prinsip pengembangan Kawasan merupakan visi dan misi pengembangan Kawasan.</li>

        <li>Konsep dan rencana Kawasan memenuhi kriteria perencanaan.</li>

        <li>Strategi perwujudan Penataan Ruang Kawasan memuat indikasi program yang terdiri atas:
            <ol>
                <li>program kegiatan pengembangan yang akan dilaksanakan;</li>

                <li>lokasi pelaksanaan pengembangan;</li>

                <li>waktu pelaksanaan pengembangan dalam jangka pendek, jangka menengah dan jangka panjang; dan</li>

                <li>penanggung jawab atau pelaksana pengembangan program dan pembiayaan.</li>

            </ol>
        </li>

        <li>Kajian Dokumen Dokumen panduan rancang Kawasan sebagaimana dimaksud dalam Pasal 187 didukung dengan kajian lain sesuai dengan kebutuhan dan karakteristik Kawasan meliputi:
            <ol>
                <li>kajian sosial dan ekonomi;</li>

                <li>kajian lingkungan hidup;</li>

                <li>kajian transportasi;</li>

                <li>kajian daya dukung dan daya tampung Kawasan;</li>

                <li>kajian potensi pengembangan Kawasan;</li>

                <li>kajian tata air;</li>

                <li>kajian properti;</li>

                <li>kajian Amdal Kawasan; dan/atau</li>

                <li>kajian lainnya yang diperlukan.</li>

            </ol>
        </li>

    </ol>

    <p style=" font-style: italic;">
        <strong>Sumber</strong> Pasal 187 - 189, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta
    </p>

