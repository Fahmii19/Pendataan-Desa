    <h4>Pengelolaan Kawasan Berorientasi Transit</h4>

    <ol style="list-style-type: lower-alpha;">

        <li>Pengelola Kawasan dalam Kawasan Berorientasi Transit merupakan Pemerintah Provinsi DKI Jakarta, Badan Usaha Swasta, Badan Usaha Milik Negara dan/ atau Badan Usaha Milik Daerah.</li>

        <li>Badan Usaha Milik Negara atau Badan Usaha Swasta bekerja sama dengan Badan Usaha Milik Daerah.</li>

        <li>Pengelola Kawasan memiliki tugas:
            <ol>
                <li>memprakarsai penyusunan dokumen panduan rancang Kawasan;</li>

                <li>mewujudkan rencana pembangunan Kawasan yang telah ditetapkan;</li>

                <li>merekomendasikan pembangunan pada Kawasan Berorientasi Transit berdasarkan panduan rancang Kawasan dan rencana pembangunan Kawasannya;</li>

                <li>mendorong Masyarakat atau stakeholder untuk mewujudkan pembangunan sesuai kriteria perencanaan dan berkoordinasi dengan Perangkat Daerah terkait;</li>

                <li>mengoperasikan dan memelihara bangunan interkoneksi pada Lahan yang menjadi aset Pemerintah Provinsi DKI Jakarta sesuai dengan ketentuan peraturan perundang - undangan;</li>

                <li>melakukan sosialisasi dokumen panduan rancang Kawasan yang sudah ditetapkan kepada seluruh stakeholder dalam cakupan radius Kawasan;</li>

                <li>untuk Kawasan Berorientasi Transit berkolaborasi dengan stakeholder paling sedikit 50 (lima puluh) persen dari cakupan luas Kawasan yang bersedia dalam pengembangan Kawasan sesuai prinsip dasar Kawasan;</li>

                <li>untuk Kawasan Berorientasi Transit berkolaborasi dengan seluruh stakeholder dari cakupan luas Kawasan yang bersedia dalam pengembangan Kawasan sesuai prinsip dasar Kawasan; dan</li>

                <li>melaporkan pelaksanaan dan realisasi pembangunan sesuai dokumen panduan rancang Kawasan kepada Perangkat Daerah yang menyelenggarakan urusan pemerintahan di bidang Penataan Ruang dan Bangunan Gedung.</li>


            </ol>
        </li>

        <li>Stakeholder pada Kawasan Berorientasi Transit dan Kawasan Berorientasi Transit mewujudkan rencana pembangunan berdasarkan panduan rancang Kawasan.</li>


    </ol>

    <p style=" font-style: italic;">
        <strong>Sumber</strong> Pasal 186, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun
        2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta
    </p>

