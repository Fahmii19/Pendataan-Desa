    <h4>Proposal Dokumen Pengembangan Kawasan Pada Zona Ambang</h4>

    <p>Proposal dokumen pengembangan kawasan terdiri atas:</p>

    <ul>
        <li style="list-style-type: lower-alpha;">
            Gambaran umum pengembangan Kawasan merupakan kondisi eksisting dalam Lahan Perencanaan.</li>
        <li style="list-style-type: lower-alpha;">
            Prinsip pengembangan Kawasan merupakan visi dan misi pengembangan kawasan.</li>
        <li style="list-style-type: lower-alpha;">
            Konsep dan rencana Kawasan memenuhi kriteria perencanaan.</li>
        <li style="list-style-type: lower-alpha;">
            Strategi perwujudan Penataan Ruang Kawasan memuat indikasi
            program yang terdiri atas:
            <ol>
                <li>
                    program kegiatan pengembangan yang akan dilaksanakan;
                </li>
                <li>lokasi pelaksanaan pengembangan;</li>
                <li>waktu pelaksanaan pengembangan dalam jangka pendek, jangka menengah dan jangka panjang; dan</li>
                <li>penanggung jawab atau pelaksana pengembangan program dan pembiayaan.</li>
            </ol>
        <li style="list-style-type: lower-alpha;">
            Kajian Proposal dokumen Pengembangan Kawasan didukung dengan kajian lain sesuai dengan kebutuhan dan karakteristik kawasan meliputi:</li>
        <ol>
            <li>
                kajian sosial dan ekonomi;</li>
            <li>kajian lingkungan hidup;</li>

            <li>kajian transportasi;</li>

            <li>kajian daya dukung dan daya tampung Kawasan;</li>

            <li>kajian potensi pengembangan Kawasan;</li>

            <li>kajian tata air;</li>

            <li>kajian properti; dan</li>

            <li>kajian lainnya sesuai yang diperlukan sesuai karakteristik kawasan.</li>


        </ol>
        </li>

    </ul>

    <p>
        <strong>Sumber</strong> Pasal 194 - 195, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta
    </p>

