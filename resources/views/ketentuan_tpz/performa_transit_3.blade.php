    <h4>Kriteria Perencanaan Kawasan Berorientasi Transit</h4>
    <p>Kawasan Berorientasi Transit berlaku kriteria perencanaan paling sedikit meliputi:</p>


    <ol style="list-style-type: lower-alpha;">

        <li>Jalur pejalan kaki dan fasilitasnya yang terintegrasi, aman dan nyaman (Ketentuan Rinci Penyediaan dapat dilihat pada pasal 184 Ayat 1 , Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta);</li>

        <li>Jalur Sepeda dan fasilitasnya yang terintegrasi, aman dan nyaman (Ketentuan Rinci Penyediaan dapat dilihat pada pasal 184 Ayat 2 , Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta);</li>

        <li>Konektivitas dan permeabilitas Kawasan yang terintegrasi (Ketentuan Rinci Penyediaan dapat dilihat pada pasal 184 Ayat 3, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta);</li>

        <li>Tata massa bangunan yang padat dan Berorientasi Transit (Ketentuan Rinci Penyediaan dapat dilihat pada pasal 184 Ayat 4, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta);</li>

        <li>Kegiatan Pemanfaatan Ruang campuran (Ketentuan Rinci Penyediaan dapat dilihat pada pasal 184 Ayat 5, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta);</li>

        <li>Menyediakan infrastruktur dasar (Ketentuan Rinci Penyediaan dapat dilihat pada pasal 184 Ayat 6 , Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta);</li>

        <li>Menyediakan RTH dan badan air (Ketentuan Rinci Penyediaan dapat dilihat pada pasal 184 Ayat 7, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta);</li>

        <li>Menyediakan Ruang publik (Ketentuan Rinci Penyediaan dapat dilihat pada pasal 184 Ayat 8 , Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta);</li>

        <li>Menyediakan signage yang jelas dan lengkap (Ketentuan Rinci Penyediaan dapat dilihat pada pasal 184 Ayat 9, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta);</li>

        <li>Pembatasan Ruang parkir kendaraan bermotor (Ketentuan Rinci Penyediaan dapat dilihat pada pasal 184 Ayat 10, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta);</li>

        <li>Deliniasi Kawasan sampai dengan 800 (delapan ratus) meter dari simpul transit sarana angkutan umum massal (Ketentuan Rinci Penyediaan dapat dilihat pada pasal 184 Ayat 11, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta); dan</li>

        <li>menerapkan prinsip Zero run off (Ketentuan Rinci Penyediaan dapat dilihat pada pasal 184 Ayat 12, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta);</li>

        <li>Kawasan Berorientasi Transit dapat dilaksanakan melalui konsolidasi tanah.</li>


    </ol>

