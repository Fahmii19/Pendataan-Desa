    <h4>Pengajuan Dokumen Rancang Bangun Untuk Kawasan Kompak</h4>

    <p>Pengajuan dokumen rancang bangun kawasan kompak dilakukan dengan:</p>

    <ol style="list-style-type: lower-alpha;">

        <li>Dokumen panduan rancang Kawasan Kompak merupakan
            proposal yang diajukan oleh calon Pengelola Kawasan kepada
            Kepala Perangkat Daerah yang menyelenggarakan urusan
            pemerintahan di bidang Penataan Ruang dan Bangunan Gedung.

        </li>
        <li>Proposal selanjutnya disampaikan kepada FPRD untuk
            mendapatkan pertimbangan.

        </li>
        <li>Hasil pertimbangan FPRD disampaikan kepada Gubernur untuk
            mendapatkan Persetujuan.

        </li>
        <li>Dalam hal Gubernur menyetujui hasil pertimbangan FPRD
            sebagaimana dimaksud pada ayat (3) penetapan lokasi dan
            Pengelola Kawasan Kompak ditetapkan dengan Keputusan
            Gubernur.

        </li>
        <li>Keputusan Gubernur tentang penetapan lokasi dan Pengelola
            Kawasan Berorientasi Transit atau Kawasan Kompak menjadi
            bahan evaluasi dalam peninjauan kembali RDTR.
        </li>

    </ol>

    <p style=" font-style: italic;">
        <strong>Sumber</strong> Pasal 190, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun
        2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta
    </p>

