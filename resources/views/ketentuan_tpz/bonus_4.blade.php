    <h4>Pengajuan Peningkatan Besaran Intensitas di Zona Bonus</h4>

    <ol style="list-style-type: lower-alpha;">

        <li>Zona Bonus diajukan oleh Pemohon bersamaan dengan permohonan Perizinan Berusaha melalui Sistem OSS.</li>

        <li>Sistem OSS memberikan notifikasi permohonan kepada Perangkat Daerah yang menyelenggarakan urusan pemerintahan di bidang pelayanan terpadu satu pintu yang selanjutnya menindaklanjuti kepada Kepala Perangkat Daerah yang menyelenggarakan urusan pemerintahan di bidang Penataan
            Ruang dan Bangunan Gedung untuk menentukan penyediaan Prasarana dan Sarana Umum yang dibutuhkan dan telah mendapatkan Persetujuan Gubernur.

        </li>
        <li>Persetujuan Gubernur ditetapkan dalam Keputusan Gubernur.</li>

        <li>Pada Kawasan Berorientasi Transit dan Kawasan Kompak, penentuan penyediaan Prasarana dan Sarana Umum yang dibutuhkan dilaksanakan oleh Pengelola Kawasan mengacu pada Panduan Rancang Kawasan.</li>

        <li>Penyediaan Prasarana dan Sarana Umum dapat dilaksanakan pada saat proses pengajuan Perizinan Berusaha atau setelah Perizinan Berusaha diterbitkan dengan tenggat waktu 1 (satu) tahun dan satu kali masa perpanjangan sesuai ketentuan Peraturan Perundang-undangan</li>

    </ol>

    <p style=" font-style: italic;">
        <strong>Sumber</strong> Pasal 174, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun
        2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta
    </p>

