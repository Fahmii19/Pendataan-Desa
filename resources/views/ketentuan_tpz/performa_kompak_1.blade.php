    <h4>Detail Rencana Kawasan Kompak</h4>

    <p>Detail Kriteria Kawasan Kompak meliputi:</p>

    <ol style="list-style-type: lower-alpha;">

        <li>jalur pejalan kaki dan fasilitasnya yang terintegrasi, aman dan nyaman paling sedikit memuat ketentuan:

            <ol>
                <li>berada pada permukaan tanah, bawah tanah dan/atau Ruang udara;</li>
                <li>jalur pejalan kaki di Ruang udara berupa Jembatan yang dapat dimanfaatkan untuk fungsi usaha;</li>
                <li>menjamin kontinuitas jalur pejalan kaki;</li>
                <li>menghubungkan dan mengintegrasikan jalur pejalan kaki dengan angkutan umum massal;</li>
                <li>diakses semua kalangan termasuk disabilitas;</li>
                <li>
                    lebar jalur pedestrian:
                    <ol style="list-style-type: disc;">
                        <li>pada Jalan arteri tidak kurang dari 5 (lima) meter;</li>
                        <li>pada Jalan kolektor tidak kurang dari 3,5 (tiga koma lima) meter;</li>
                        <li>pada Jalan lokal dan lingkungan tidak kurang dari 2,5 (dua koma lima) meter;</li>
                    </ol>
                </li>

                <li>
                    Perkerasan jalur pejalan kaki terbuat dari material yang kuat, tidak mudah rusak, tidak licin, mudah dirawat, mampu meresapkan air atau menggunakan perkerasan beton poros dan menyediakan guiding blocks; dan
                </li>
                <li>
                    dilengkapi dengan kelengkapan jalan yang meliputi:
                    <ol style="list-style-type: disc;">
                        <li>rambu, marka dan tata informasi;</li>
                        <li>lampu penerangan saat malam hari;</li>
                        <li>tempat duduk;</li>
                        <li>pelindung atau peneduh;</li>
                        <li>tempat sampah; dan</li>
                        <li>halte atau pemberhentian bus</li>
                    </ol>
                </li>


            </ol>

        </li>

        <li>Jalur Sepeda dan fasilitasnya yang terintegrasi, aman dan nyaman paling sedikit memuat ketentuan:
            <ol>
                <li>
                    Lebar Jalur sepeda:
                    <ol style="list-style-type: disc;">
                        <li>tidak kurang dari 1,5 (satu koma lima) meter untuk satu jalur dan 2,4 (dua koma empat) meter untuk dua jalur;</li>
                        <li>berbagi permukaan dengan kendaraan lain tidak kurang dari 2,4 (dua koma empat) meter; dan/atau</li>
                        <li>berbagi permukaan dengan jalur pejalan kaki tidak kurang dari 5 (lima) meter dan yang digunakan untuk Jalur sepeda tidak kurang dari 3 (tiga) meter.</li>
                    </ol>
                </li>
                <li>Dilengkapi dengan fasilitas Jalur Sepeda yang memadai seperti penerangan lampu, rambu dan marka jalan; dan</li>
                <li>
                    dilengkapi dengan parkir sepeda yang diletakkan di Ruang publik, Kavling privat dan/atau di dalam bangunan dengan ketentuan:
                    <ol style="list-style-type: disc;">
                        <li>dimensi parkir untuk 1 (satu) unit parkir sepeda yaitu 2 (dua) meter kali 0,6 (nol koma enam) meter;</li>
                        <li>berada di Jalan atau koridor utama Kawasan, area muara stasiun dan Ruang publik; dan</li>
                        <li>mudah dicapai.</li>
                    </ol>
                </li>
            </ol>
        </li>

        <br>

        <li>Konektivitas dan permeabilitas Kawasan yang terintegrasi paling sedikit memuat ketentuan:
            <ol>
                <li>jaringan Jalan, jalur pedestrian dan Jalur Sepeda terhubung dengan titik transportasi angkutan umum massal;</li>
                <li>menghubungkan antarbangunan di dalam Kawasan yang terintegrasi baik secara fisik maupun fungsi, serta dalam bentuk vertikal, horizontal, atau kombinasi keduanya yang dapat diakses publik;</li>
                <li>menghubungkan Jalan, jalur pejalan kaki dan Jalur Sepeda dengan Kawasan lain di luar Kawasan;</li>
                <li>membuka pagar antar-Kavling atau pembatas Ruang milik Jalan dengan bangunan dengan prinsip active frontage;</li>
                <li>dilengkapi dengan fasilitas selter moda transportasi berbasis daring; dan</li>
                <li>akses masuk bangunan utama berorientasi ke Jalan untuk memudahkan akses pejalan kaki.</li>
            </ol>
        </li>

        <li>Tata massa bangunan yang padat dan kompak paling sedikit memuat ketentuan:
            <ol>
                <li>
                    mengoptimalisasikan Pemanfaatan Ruang dengan pengembangan bangunan secara vertikal; dan
                </li>
                <li>menerapkan Bangunan Gedung hijau pada Bangunan Gedung dengan kriteria tertentu sesuai dengan ketentuan perattwan perundang-undangan paling sedikit memperhatikan reduce, reuse dan recycle sampah; efisiensi penggunaan energi dan air; penggunaan material ramah lingkungan; perlindungan dan pengelolaan terhadap lingkungan hidup; mitigasi risiko keselamatan, kesehatan, perubahan iklim dan bencana; dan inovasi teknologi untuk perbaikan lingkungan Kawasan yang berkelanjutan.</li>
            </ol>
        </li>

        <li>Kegiatan Pemanfaatan Ruang dengan fungsi campuran paling sedikit memuat ketentuan:
            <ol>
                <li>Kawasan dengan fungsi campuran;</li>

                <li>lantai dasar dapat dimanfaatkan sebagai Ruang publik atau fungsi usaha kecuali untuk industri menengah dan besar;</li>

                <li>penyediaan hunian mengakomodir penghuni eksisting yang berada di dalam delineasi Kawasan;</li>

                <li>bangunan hunian dikembangkan dengan prinsip hunian tangguh berkelanjutan; dan</li>

                <li>penyediaan hunian Masyarakat berpenghasilan rendah hingga menengah paling sedikit 20 (dua puluh) persen dari total unit yang disediakan.</li>

                <li>mitigasi risiko keselamatan, kesehatan, perubahan iklim dan bencana; dan</li>

                <li>inovasi teknologi untuk perbaikan lingkungan Kawasan yang berkelanjutan.</li>

            </ol>
        </li>

        <li>Penyediaan infrastruktur dasar paling sedikit memuat ketentuan:
            <ol>
                <li>menyediakan jaringan air bersih dan jaringan listrik;</li>

                <li>menyediakan SPALD-S atau water treatment plant;</li>

                <li>pengambilan air tanah tidak diperbolehkan sepanjang sudah terlayani jaringan perpipaan air bersih;</li>

                <li>menyediakan sistem pemadam kebakaran aktif dan/ atau pasif di setiap bangunan;</li>

                <li>menyediakan pengolahan limbah secara mandiri berupa TPS limbah B3, TPS-3R dan/atau FPSA mikro; dan</li>

                <li>mendorong penggunaan kendaraan ramah lingkungan dengan penyediaan SPKLU atau SPBKLU.</li>

            </ol>
        </li>

        <br>

        <li>
            Penyediaan RTH dan badan air paling sedikit memuat ketentuan:
            <ol>
                <li>menyediakan RTH paling sedikit 20 (dua puluh) persen dari luas Lahan;</li>

                <li>pemecahan RTH sedikit 1.000 (seribu) meter persegi secara utuh;</li>

                <li>luas tajuk hijau paling sedikit 40 (empat puluh) persen dari luas Kawasan diutamakan menanam pohon pelindung;</li>

                <li>ramah pejalan kaki dan penyandang disabilitas;</li>

                <li>mudah dicapai, nyaman dan mewadahi kegiatan yang aktif dan atraktif dengan tetap menerapkan fungsi ekologis, sosiologis dan rekreasi;</li>

                <li>dilengkapi dengan fasilitas olahraga dan fasilitas penunjang protokol kesehatan seperti tempat untuk mencuci tangan, bangku, toilet dan tempat sampah yang dibedakan sesuai dengan tipe sampah;</li>

                <li>memiliki penerangan yang cukup;</li>

                <li>menyediakan kolam retensi dan/atau SDEW paling sedikit 5 (lima) persen dari keseluruhan luas Kawasan sebagai pengendali banjir Kawasan dan terhubung dengan saluran drainase primer, sekunder dan/atau tersier; dan</li>

                <li>menyediakan sumur resapan sesuai ketentuan yang dipersyaratkan.</li>
            </ol>
        </li>

        <li>Penyediaan Ruang publik memuat ketentuan:
            <ol>
                <li>berupa Ruang terbuka dan/atau tertutup seperti RTH, plaza, alun-alun, sarana olahraga, taman bermain dan rekreasi, Ruang di bawah Jalan layang, Ruang antar-Bangunan Gedung, jalur hijau, sirkulasi dalam bangunan dan/atau Lahan parkir;</li>

                <li>digunakan untuk mewadahi kegiatan yang aktif dan atraktif seperti pagelaran seni, penyediaan Ruang usaha UMKM, pameran, titik berkumpul terutama pada saat terjadi evakuasi bencana dan aktivitas publik lainnya;</li>

                <li>ramah pejalan kaki dan penyandang disabilitas;</li>

                <li>dilengkapi dengan Prasarana pendukung berbasis digital seperti jaringan nirkabel atau spot wifi access point, kamera pengawas dan stasiun pengisian baterai perangkat elektronik; dan</li>

                <li>dilengkapi dengan wastafel, tempat sampah, bangku, tempat bersandar dan penerangan yang baik.</li>

            </ol>
        </li>

        <li>
            Penyediaan signage yang jelas dan lengkap paling sedikit memuat ketentuan:
            <ol>
                <li>berupa petunjuk arah, peta jalur evakuasi, tata informasi dan/atau reklame terutama pengarah ke pusat kegiatan dan titik angkutan umum;</li>

                <li>diletakkan pada fasad bangunan, di atas pintu masuk, dinding dan/atau koridor;</li>

                <li>ukuran huruf pada rambu atau penanda terlihat jelas;</li>

                <li>diletakkan di tempat yang terlihat jelas serta tidak mengganggu visual dan sirkulasi pejalan kaki; dan</li>

                <li>dilengkapi dengan pencahayaan yang memadai sehingga terlihat di malam hari.</li>

            </ol>
        </li>

        <li>Parkir paling sedikit memuat ketentuan:
            <ol>
                <li>
                    tidak diperbolehkan parkir di badan Jalan kecuali:
                    <ol style="list-style-type: disc;">
                        <li>tidak pada Jalan arteri dan kolektor;</li>
                        <li>lebar Jalan paling sedikit 12 (dua belas) meter; dan</li>

                        <li>tidak terletak pada jalur bus non koridor.</li>

                    </ol>
                </li>
                <li>tidak diperbolehkan menghalangi jalur pejalan kaki;</li>

                <li>dapat dilakukan penyediaan parkir melalui fasilitas parkir bersama;</li>

                <li>material penutup Lahan parkir tanpa atap menggunakan material yang mampu meresap air;</li>

                <li>penyediaan fasilitas pendukung seperti kerb, petunjuk Jalan, rambu, tata informasi dan sistem keamanan parkir; dan</li>

                <li>area lantai dasar fasilitas parkir pada podium bangunan yang berbatasan langsung dengan jalur pejalan kaki dapat dimanfaatkan menjadi fungsi usaha.</li>

            </ol>
        </li>

        <li>Kepadatan penduduk tinggi sebagaimana dimaksud dalam Pasal 179 huruf k yaitu memenuhi kepadatan penduduk paling sedikit 450 (empat ratus lima puluh) jiwa per hektare.</li>

        <li>Penerapan prinsip Zero run off pada Kawasan Kompak dengan membuat fasilitas penampungan air limpasan dalam LP maupun dalam delineasi K awasan Kompak.</li>


    </ol>

    <p style=" font-style: italic;">
        <strong>Sumber</strong> Pasal 180, Peraturan Gubernur DKI Jakarta Nomor 31 Tahun 2022 tentang Rencana Detail Tata Ruang (RDTR) DKI Jakarta
    </p>

