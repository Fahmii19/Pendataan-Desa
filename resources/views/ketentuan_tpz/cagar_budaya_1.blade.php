    <h4>Ketentuan Pelestarian Kawasan Cagar Budaya (l)</h4>

    <ol style="list-style-type: lower-alpha;">

        <li>Ketentuan Pemeliharaan Kawasan Cagar Budaya Pemeliharaan dilakukan dengan mencegah kerusakan karena faktor manusia dan/atau alam yang mengakibatkan berubahnya keaslian dan nilai-nilai yang menyertainya.</li>

        <li>Ketentuan Perawatan Kawasan Cagar Budaya Perawatan dilakukan dengan pembersihan, pengawetan dan perbaikan atas kerusakan dengan memperhatikan keaslian bentuk, tata letak, gaya, bahan dan/atau teknologi cagar budaya.</li>

        <li>Ketentuan Pemugaran Kawasan Cagar Budaya:

            <ol>
                <li>
                    Mekanisme Pemugaran Kawasan Cagar Budaya Pemugaran dilakukan untuk mengembalikan kondisi fisik dengan cara memperbaiki, memperkuat dan/atau mengawetkannya melalui pekerjaan rekonstruksi, konsolidasi,
                    rehabilitasi dan restorasi meliputi :
                    <ol style="list-style-type: lower-alpha;">
                        <li>
                            keaslian bahan, bentuk, tata letak, gaya dan/atau teknologi pengerjaan;
                        </li>
                        <li>kondisi semula dengan tingkat perubahan sekecil mungkin;</li>
                        <li>penggunaan teknik, metode dan bahan yang tidak bersifat
                            merusak; dan

                        </li>
                        <li>kompetensi pelaksana di bidang pemugaran.</li>

                    </ol>

                </li>
                <li>Pemugaran yang berpotensi menimbulkan dampak negatif
